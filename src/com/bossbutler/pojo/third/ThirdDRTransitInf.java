package com.bossbutler.pojo.third;

public class ThirdDRTransitInf {

	private String transitID;
	private String transitTime;
	private String transitType;
	private String arrangeName;
	private String entranceType;

	public String getTransitID() {
		return transitID;
	}

	public void setTransitID(String transitID) {
		this.transitID = transitID;
	}

	public String getTransitTime() {
		return transitTime;
	}

	public void setTransitTime(String transitTime) {
		this.transitTime = transitTime;
	}

	public String getTransitType() {
		return transitType;
	}

	public void setTransitType(String transitType) {
		this.transitType = transitType;
	}

	public String getArrangeName() {
		return arrangeName;
	}

	public void setArrangeName(String arrangeName) {
		this.arrangeName = arrangeName;
	}

	public String getEntranceType() {
		return entranceType;
	}

	public void setEntranceType(String entranceType) {
		this.entranceType = entranceType;
	}

}
