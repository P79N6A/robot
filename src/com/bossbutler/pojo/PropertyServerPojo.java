package com.bossbutler.pojo;

public class PropertyServerPojo {
	private String tittle;// 标题
	private String name;// 接单人
	private String monadtime;// 创建时间
	private String monadrype;// 服务单类型
	private String address;// 服务地址 待接单的时候有值
	private String type;// 状态 自主接单还是被指派 已指派
	private String serverId;// 服务单Id
	private String servercode;// 服务单单号
	private String ck;// 查看权限 1.有该权限 2.没有
	private String pd;// 派单权限 1.有该权限 2.没有
	private String jd;// 接单权限 1.有该权限 2.没有

	private String statusCode;// 状态代码（01，初始化；02，已提交；03，已派单；04，被指派；05，已撤回；06，已接单；07，已完成；08，已评价；09，已取消）

	public String getServercode() {
		return servercode;
	}

	public void setServercode(String servercode) {
		this.servercode = servercode;
	}

	public String getCk() {
		return ck;
	}

	public void setCk(String ck) {
		this.ck = ck;
	}

	public String getPd() {
		return pd;
	}

	public void setPd(String pd) {
		this.pd = pd;
	}

	public String getJd() {
		return jd;
	}

	public void setJd(String jd) {
		this.jd = jd;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTittle() {
		return tittle;
	}

	public void setTittle(String tittle) {
		this.tittle = tittle;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMonadtime() {
		return monadtime;
	}

	public void setMonadtime(String monadtime) {
		this.monadtime = monadtime;
	}

	public String getMonadrype() {
		return monadrype;
	}

	public void setMonadrype(String monadrype) {
		this.monadrype = monadrype;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getServerId() {
		return serverId;
	}

	public void setServerId(String serverId) {
		this.serverId = serverId;
	}

	public String getStatusCode() {
		return statusCode;
	}

	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}

}
