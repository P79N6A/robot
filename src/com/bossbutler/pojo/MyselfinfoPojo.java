package com.bossbutler.pojo;

import java.util.List;

public class MyselfinfoPojo {
	
	private String accountId;// 登陆人ID
	private String headpath;// 头像
	private String name;// 姓名
	private String realname;// 真实姓名
	private String loginId;//登录名
	private String sex;// 性别
	private String phone;// 电话
	private String score;// 评分
	private String billCount;// 接单数据
	private List<MyCompany> companies;

	public String getAccountId() {
		return accountId;
	}

	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}

	public String getHeadpath() {
		return headpath;
	}

	public void setHeadpath(String headpath) {
		this.headpath = headpath;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getRealname() {
		return realname;
	}

	public void setRealname(String realname) {
		this.realname = realname;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getBillCount() {
		return billCount;
	}

	public void setBillCount(String billCount) {
		this.billCount = billCount;
	}

	public List<MyCompany> getCompanies() {
		return companies;
	}

	public void setCompanies(List<MyCompany> companies) {
		this.companies = companies;
	}

	public String getLoginId() {
		return loginId;
	}

	public void setLoginId(String loginId) {
		this.loginId = loginId;
	}

}
