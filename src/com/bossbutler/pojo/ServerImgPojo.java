package com.bossbutler.pojo;

public class ServerImgPojo {
	private String mediaId;
	private String billId;//服务单Id
	private String mediaType;//媒体类别
	private String fileType;//文件类别
	private String main_classify;
	private String createtime;//创建时间
	private String updatetime;//更新时间
	private String ordinal;//排列序号
	public String getMediaId() {
		return mediaId;
	}
	public void setMediaId(String mediaId) {
		this.mediaId = mediaId;
	}
	public String getBillId() {
		return billId;
	}
	public void setBillId(String billId) {
		this.billId = billId;
	}
	public String getMediaType() {
		return mediaType;
	}
	public void setMediaType(String mediaType) {
		this.mediaType = mediaType;
	}
	public String getFileType() {
		return fileType;
	}
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	public String getMain_classify() {
		return main_classify;
	}
	public void setMain_classify(String main_classify) {
		this.main_classify = main_classify;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
	public String getUpdatetime() {
		return updatetime;
	}
	public void setUpdatetime(String updatetime) {
		this.updatetime = updatetime;
	}
	public String getOrdinal() {
		return ordinal;
	}
	public void setOrdinal(String ordinal) {
		this.ordinal = ordinal;
	}
	
}
