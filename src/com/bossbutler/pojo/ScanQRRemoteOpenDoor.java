package com.bossbutler.pojo;

/**
 * 扫描二维码开门参数
 * 
 * @author wangqiao
 */
public class ScanQRRemoteOpenDoor {

	private String qrdata;// 二维码通行码
	private String userType;// 用户类型【常驻（00）或者访客（05）】
	private String transitCode;// 二维码开门通行码
	private String aId;// 常驻accountId或者访客applyId
	private String longitude;// 手机端定位经度
	private String latitude;// 手机端定位维度

	public String getQrdata() {
		return qrdata;
	}

	public void setQrdata(String qrdata) {
		this.qrdata = qrdata;
	}

	public String getUserType() {
		return userType;
	}

	public void setUserType(String userType) {
		this.userType = userType;
	}

	public String getTransitCode() {
		return transitCode;
	}

	public void setTransitCode(String transitCode) {
		this.transitCode = transitCode;
	}

	public String getaId() {
		return aId;
	}

	public void setaId(String aId) {
		this.aId = aId;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

}
