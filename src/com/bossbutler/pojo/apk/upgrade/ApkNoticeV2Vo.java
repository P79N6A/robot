package com.bossbutler.pojo.apk.upgrade;

/**
 * apk 升级新版本信息表
 * 
 * @author appClient
 */
public class ApkNoticeV2Vo extends ApkNoticeVo {

	private String terminalId; // 手机唯一标识符
	private String terminalMac; // 手机mac
	private String terminalNet; // 手机网络（wifi/4G等）
	private String terminalModelAVersion; // 手机型号以及版本号

	public String getTerminalId() {
		return terminalId;
	}

	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}

	public String getTerminalMac() {
		return terminalMac;
	}

	public void setTerminalMac(String terminalMac) {
		this.terminalMac = terminalMac;
	}

	public String getTerminalNet() {
		return terminalNet;
	}

	public void setTerminalNet(String terminalNet) {
		this.terminalNet = terminalNet;
	}

	public String getTerminalModelAVersion() {
		return terminalModelAVersion;
	}

	public void setTerminalModelAVersion(String terminalModelAVersion) {
		this.terminalModelAVersion = terminalModelAVersion;
	}

}