package com.bossbutler.pojo.apk.upgrade;

/**
 * 持久化对象·apk 信息表
 * 
 * @author horizon
 */
public class TbApkNoticePo {

	private Integer id;
	private String noticeType; // 通知类型 01-强制 02-警告 03-新版本 04-通知
	private String noticeWay; // 通知方式：01-弹窗 02-跑马灯
	private String fontColor; // 字体颜色
	private String noticeContent; // 通知内容

	private Integer displayTime; // 显示时间
	private Integer appTypeId; // app类型ID
	private String appVersion; // 适应app版本
	private String appSystem; // 设备系统 01：安卓 02：IOS

	private String downloadPathAndroid; // 代码
	private String downloadPathIos; // 类型名称
	private String skipPath; // 二维码路径
	private String code; // 应用代码
	private String htmlTitle;
	private String versionName;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNoticeType() {
		return noticeType;
	}

	public void setNoticeType(String noticeType) {
		this.noticeType = noticeType;
	}

	public String getNoticeWay() {
		return noticeWay;
	}

	public void setNoticeWay(String noticeWay) {
		this.noticeWay = noticeWay;
	}

	public String getFontColor() {
		return fontColor;
	}

	public void setFontColor(String fontColor) {
		this.fontColor = fontColor;
	}

	public String getNoticeContent() {
		return noticeContent;
	}

	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}

	public Integer getDisplayTime() {
		return displayTime;
	}

	public void setDisplayTime(Integer displayTime) {
		this.displayTime = displayTime;
	}

	public Integer getAppTypeId() {
		return appTypeId;
	}

	public void setAppTypeId(Integer appTypeId) {
		this.appTypeId = appTypeId;
	}

	public String getAppVersion() {
		return appVersion;
	}

	public void setAppVersion(String appVersion) {
		this.appVersion = appVersion;
	}

	public String getAppSystem() {
		return appSystem;
	}

	public void setAppSystem(String appSystem) {
		this.appSystem = appSystem;
	}

	public String getDownloadPathAndroid() {
		return downloadPathAndroid;
	}

	public void setDownloadPathAndroid(String downloadPathAndroid) {
		this.downloadPathAndroid = downloadPathAndroid;
	}

	public String getDownloadPathIos() {
		return downloadPathIos;
	}

	public void setDownloadPathIos(String downloadPathIos) {
		this.downloadPathIos = downloadPathIos;
	}

	public String getSkipPath() {
		return skipPath;
	}

	public void setSkipPath(String skipPath) {
		this.skipPath = skipPath;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getHtmlTitle() {
		return htmlTitle;
	}

	public void setHtmlTitle(String htmlTitle) {
		this.htmlTitle = htmlTitle;
	}

	public String getVersionName() {
		return versionName;
	}

	public void setVersionName(String versionName) {
		this.versionName = versionName;
	}

}