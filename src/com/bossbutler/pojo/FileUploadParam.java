package com.bossbutler.pojo;

import java.io.Serializable;

/**
 * <p>
 * 上传文件服务器实体类.
 * </p>
 * 
 * @author wq
 *
 */
public class FileUploadParam implements Serializable {

	private static final long serialVersionUID = 147017140157550424L;

	private String mainClassify;
	private String bussinessID;
	private String fileType;
	private String medium;
	private String little;
	private String createTime;
	private String mediaID;

	public String getMainClassify() {
		return mainClassify;
	}

	public void setMainClassify(String mainClassify) {
		this.mainClassify = mainClassify;
	}

	public String getBussinessID() {
		return bussinessID;
	}

	public void setBussinessID(String bussinessID) {
		this.bussinessID = bussinessID;
	}

	public String getFileType() {
		return fileType;
	}

	public void setFileType(String fileType) {
		this.fileType = fileType;
	}

	public String getMedium() {
		return medium;
	}

	public void setMedium(String medium) {
		this.medium = medium;
	}

	public String getLittle() {
		return little;
	}

	public void setLittle(String little) {
		this.little = little;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getMediaID() {
		return mediaID;
	}

	public void setMediaID(String mediaID) {
		this.mediaID = mediaID;
	}

}
