package com.bossbutler.pojo.news;

import java.io.Serializable;
import java.util.Date;

public class NewsLogModel implements Serializable {
	/**
	 */
	private static final long serialVersionUID = -4219269407120726618L;
	private String logId;
	private String newsId;
	private String accountId;
	private String statusCode;
	private String statusRemark;
	private Date statusTime;
	private Date createTime;
	private Date updateTime;
	private Date planTime;
	private Long operatorId;
	private Date dataVersion;
	
	
	
	public String getLogId() {
		return logId;
	}
	public void setLogId(String logId) {
		this.logId = logId;
	}
	public String getNewsId() {
		return newsId;
	}
	public void setNewsId(String newsId) {
		this.newsId = newsId;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	
	public String getStatusRemark() {
		return statusRemark;
	}
	public void setStatusRemark(String statusRemark) {
		this.statusRemark = statusRemark;
	}
	public Date getStatusTime() {
		return statusTime;
	}
	public void setStatusTime(Date statusTime) {
		this.statusTime = statusTime;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public Date getPlanTime() {
		return planTime;
	}
	public void setPlanTime(Date planTime) {
		this.planTime = planTime;
	}
	public Long getOperatorId() {
		return operatorId;
	}
	public void setOperatorId(Long operatorId) {
		this.operatorId = operatorId;
	}
	public Date getDataVersion() {
		return dataVersion;
	}
	public void setDataVersion(Date dataVersion) {
		this.dataVersion = dataVersion;
	}


}