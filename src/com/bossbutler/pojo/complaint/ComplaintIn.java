package com.bossbutler.pojo.complaint;

/**
 * 用于放服务单列表参数
 * 
 * @author 98030
 *
 */
public class ComplaintIn {
	
	private String pageNo;
	private String pageSize;

	private String infoType;// 信息类型（01，投诉；02，建议；）
	private String orgId;// 物业公司
	private String accountId;//帐号ID
	private String complaintId;// 服务单ID
	private String replyContents;// 回复内容
	
	
	public String getInfoType() {
		return infoType;
	}
	public void setInfoType(String infoType) {
		this.infoType = infoType;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getComplaintId() {
		return complaintId;
	}
	public void setComplaintId(String complaintId) {
		this.complaintId = complaintId;
	}
	public String getPageNo() {
		return pageNo;
	}
	public void setPageNo(String pageNo) {
		this.pageNo = pageNo;
	}
	public String getPageSize() {
		return pageSize;
	}
	public void setPageSize(String pageSize) {
		this.pageSize = pageSize;
	}
	public String getReplyContents() {
		return replyContents;
	}
	public void setReplyContents(String replyContents) {
		this.replyContents = replyContents;
	}
	

	
}
